export function localizePost(post, language) {
  if (!post) {
    return post;
  }

  const readingTimeDigits = String(post.readingTime || "").replace(/\D+/g, "");
  const baseZh = {
    ...post,
    readingTime: readingTimeDigits ? `${readingTimeDigits} 分钟` : post.readingTime,
    recommendedFor: post.recommendedForZh || post.recommendedForEn,
  };

  if (language !== "en") {
    return {
      ...baseZh,
      relatedPosts: (post.relatedPosts || []).map((item) => localizePost(item, language)),
      prevPost: localizePost(post.prevPost, language),
      nextPost: localizePost(post.nextPost, language),
    };
  }

  const translated = postTranslations[post.slug];
  return {
    ...baseZh,
    ...(translated || {}),
    category: translateCategoryName(post.category, language),
    tags: post.tags.map((tag) => translateTagName(tag, language)),
    recommendedFor: post.recommendedForEn || post.recommendedForZh,
    readingTime: readingTimeDigits ? `${readingTimeDigits} min read` : post.readingTime,
    relatedPosts: (post.relatedPosts || []).map((item) => localizePost(item, language)),
    prevPost: localizePost(post.prevPost, language),
    nextPost: localizePost(post.nextPost, language),
  };
}

export function localizeMessage(message, language) {
  if (!message || language !== "en") {
    return message;
  }

  return {
    ...message,
    content: messageTranslations[message.id] || message.content,
  };
}
