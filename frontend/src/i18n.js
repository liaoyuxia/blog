export function localizeProfile(profile, language) {
  if (!profile) {
    return profile;
  }

  const replaceLegacyCopy = (value) => {
    if (!value) {
      return value;
    }

    return value
      .replace("专门的内容页", "文章归档")
      .replace("进入内容页看文章、筛选主题或留下反馈，首页与内容页职责分离得更清晰。", "进入文章归档阅读、筛选主题或留下反馈，首页与归档页的职责会更清晰。")
      .replace("跳转到内容页继续阅读与留言", "跳转到文章归档继续阅读与留言")
      .replace("dedicated journal page", "archive")
      .replace("journal page for browsing, filtering, and leaving a message", "article archive for browsing, filtering, and reader feedback")
      .replace("entry points into the journal page", "entry points into the article archive");
  };

  const normalizedName =
    profile.name === "Lin Studio" ? "Bing Studio" : (profile.name || profile.brandName);
  const normalizedEmail =
    profile.email === "hello@linstudio.dev" ? "hello@bingstudio.dev" : profile.email;

  if (language !== "en") {
    return {
      ...profile,
      name: normalizedName,
      role: profile.roleZh || profile.role,
      bio: profile.bioZh || profile.bio,
      location: profile.locationZh || profile.location,
      email: normalizedEmail,
      specialties: profile.specialtiesZh || profile.specialties || [],
      homeAboutTitle: profile.homeAboutTitleZh,
      homeAboutDescription: replaceLegacyCopy(profile.homeAboutDescriptionZh),
      homePillarsTitle: profile.homePillarsTitleZh,
      homePillars: (profile.homePillarsZh || []).map(replaceLegacyCopy),
      homeJourneyTitle: profile.homeJourneyTitleZh,
      homeJourneyDescription: replaceLegacyCopy(profile.homeJourneyDescriptionZh),
      footerProduct: profile.footerProductZh || publicCopy.zh.footerProduct,
      footerStack: profile.footerStackZh || publicCopy.zh.footerStack,
    };
  }

  return {
    ...profile,
    name: normalizedName,
    role: profile.roleEn || profile.role,
    bio: profile.bioEn || profile.bio,
    location: profile.locationEn || profile.location,
    email: normalizedEmail,
    specialties: profile.specialtiesEn || profile.specialties || [],
    homeAboutTitle: profile.homeAboutTitleEn || profile.homeAboutTitleZh,
    homeAboutDescription: replaceLegacyCopy(profile.homeAboutDescriptionEn || profile.homeAboutDescriptionZh),
    homePillarsTitle: profile.homePillarsTitleEn || profile.homePillarsTitleZh,
    homePillars: (profile.homePillarsEn || profile.homePillarsZh || []).map(replaceLegacyCopy),
    homeJourneyTitle: profile.homeJourneyTitleEn || profile.homeJourneyTitleZh,
    homeJourneyDescription: replaceLegacyCopy(profile.homeJourneyDescriptionEn || profile.homeJourneyDescriptionZh),
    footerProduct: profile.footerProductEn || publicCopy.en.footerProduct,
    footerStack: profile.footerStackEn || publicCopy.en.footerStack,
  };
}

export function localizeCategoryItem(category, language) {
  return {
    ...category,
    label: translateCategoryName(category.name, language),
  };
}

export function localizeTagItem(tag, language) {
  return {
    ...tag,
    label: translateTagName(tag.name, language),
  };
}

export function localizePost(post, language) {
  if (!post) {
    return post;
  }

  if (language !== "en") {
    return post;
  }

  const translated = postTranslations[post.slug];
  return {
    ...post,
    ...(translated || {}),
    category: translateCategoryName(post.category, language),
    tags: post.tags.map((tag) => translateTagName(tag, language)),
    readingTime: post.readingTime?.replace("分钟", "min read"),
  };
}

export function localizeMessage(message, language) {
  if (!message || language !== "en") {
    return message;
  }

  return {
    ...message,
    content: messageTranslations[message.id] || message.content,
  };
}
